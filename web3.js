async function connectWallet(provider="metamask") {
  try {
    let ethereumProvider;

    if(provider==="metamask" && window.ethereum && window.ethereum.isMetaMask) {
      ethereumProvider = window.ethereum;
    } else if(provider==="okx" && window.okxwallet) {
      ethereumProvider = window.okxwallet;
    } else if(provider==="bitget" && window.bitkeep && window.bitkeep.ethereum) {
      ethereumProvider = window.bitkeep.ethereum;
    } 

    await ethereumProvider.request({ method: "eth_requestAccounts" });
    web3 = new Web3(ethereumProvider);
    const accounts = await web3.eth.getAccounts();
    routerContract = new web3.eth.Contract(ROUTER_ABI, ROUTER_ADDRESS);
    return accounts[0];

  } catch(err){
    console.error("Connect Wallet Error:", err);
    return null;
  }
}

